<?php
    // 这里写一个求斐波那契数列第n项的值得算法；
    // 算法步骤：
    // 1. 确定初始值；
    // 2. 循环做递推；
    
    $n = 1000;
    $re = a($n);
    echo $re;

    function a($n){
        $f = array();
        // 1. 确定初始值；
        $f[0] = 0;
        $f[1] = $f[2] = 1;
        // 2. 循环做递推；
        // 递推表示：f[i] = f[i-1] + f[i-2](2<i<=n);
        for($i=3; $i<=$n; $i++){
            $f[$i] = $f[$i-1] + $f[$i-2];
        }
        if($n == 1 || $n == 2){
            return $f[1];
        }
        return $f[$n];
    }